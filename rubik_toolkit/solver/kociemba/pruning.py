	if cached is not None:
		_flip_slice_prun = cached
		return cached
	table = _bfs_pair(
		mv.flip_move(),
		mv.slice_move(),
		cd.SLICE_COUNT,
		mv.HTM_COUNT,
		_FLIP_SLICE_SIZE,
	)
	_save_table("flip_slice_prun", table)
	_flip_slice_prun = table
	return table


